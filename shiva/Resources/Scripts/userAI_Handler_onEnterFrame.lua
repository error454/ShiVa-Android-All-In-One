function userAI.onEnterFrame (  )
--------------------------------------------------------------------------------
	
    local min = -10
    local max = 10
	dynamics.addLinearImpulse ( 
        this.hEmitter ( ), 
        math.random ( min, max ), 
        math.random ( min, max ), 
        math.random ( min, max ), 
        object.kGlobalSpace)
	
--------------------------------------------------------------------------------
end
